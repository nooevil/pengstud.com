<script>
	$(function() {
		$(document).ready(function(){
            $(".cases_tab__card:nth-child(1)").addClass("active");
            $(".navigation:nth-child(1)").addClass("active");
            
            let navArr = Array.from($(".navigation"));
            let currIndex = 0;


            function changeCase() {
                $(".navigation").removeClass("active")
                navArr[currIndex].classList.add("active")
                $(`.cases_tab__card`).removeClass("active")
                $(`.cases_tab__card:nth-child(${currIndex + 1})`).addClass("active")
                if (currIndex < navArr.length -1) {
                    currIndex++
                } else {
                    currIndex = 0;
                }
            }
            let timerId = setInterval( changeCase , 5000);
            
            $(".navigation").click(function(event){
                clearInterval(timerId);
                
                $(".navigation").removeClass("active")
                $(this).addClass("active")

                navArr.forEach((elem, idx) => {
                    elem.classList.forEach((elem) => {
                        if (elem === "active") {
                            currIndex = idx;
                        }
                    })    
                })
                $(`.cases_tab__card`).removeClass("active")
                $(`.cases_tab__card:nth-child(${currIndex + 1})`).addClass("active")
            })
		});
		
	});
</script>
