            $(window).scroll(function(){
                $(".single-vacancy-template section").each(function(){
                    if($(window).scrollTop()>($(this).offset().top - $(window).height() + 50)){
                        $(this).addClass("showed")
                    }    
                })
                $(".single-vacancy-template__block__img-container").each(function(){
                    if($(window).scrollTop()>($(this).offset().top - $(window).height() + 350)){
                        $(this).addClass("showed")
                    }    
                })
                if($(window).scrollTop()>($(".single-vacancy-template__plan__content").offset().top - $(window).height())){
                    let i = 0

                    let imgArr = Array.from($(".single-vacancy-template__plan__content__card"));

                    function myLoop () {           
                        setTimeout(function () {   
                            imgArr[i].classList.add("showed");          
                            i++;
                            if (i < imgArr.length) {   
                                myLoop();             
                            }                     
                        }, 50)
                    }

                    myLoop();
                }				
            })
		});
		
	});
