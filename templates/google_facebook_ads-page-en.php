<script>
$('.open_form').on('click', function() {
  	$('.input_invalid_1').removeClass("activated")
	$('.input_invalid_2').removeClass("activated")
	$.fancybox.open({
		src  : '#first_form',
		type : 'inline',
		opts : {
		afterShow : function( instance, current ) {
		},
		smallBtn: false,
		}
	});
});
 
$('#first_form_button').on('click', function(event) {
	event.preventDefault();
 	var target = $("select[name='target']").val()
  	var budget = $("select[name='budget']").val()
 	if (target && budget) {
 	    $.fancybox.open({
			src  : '#second_form',
			type : 'inline',
			opts : {
				afterShow : function( ) {
					$("input[name='Target']").val(target)
					$("input[name='Budget']").val(budget)
				},
				afterClose : function() {
					$.fancybox.destroy();
				},
				smallBtn: false,
			}
		});
 	}
 	if (!target) {
 		$('.input_invalid_1').addClass("activated")
 	}
 	if (!budget) {
 		$('.input_invalid_2').addClass("activated")
 	}
});

$('.modal_audit_select_holder').on('click', function() {
	$('.input_invalid_1').removeClass("activated")
	$('.input_invalid_2').removeClass("activated")
});
</script>
