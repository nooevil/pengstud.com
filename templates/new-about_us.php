		$(document).ready(function(){
            $(".new-about-us__first-screen").addClass("showed")

            $(window).scroll(function(){
                $(".new-about-us section").each(function(){
                    if($(window).scrollTop()>($(this).offset().top - $(window).height() + 50)){
                        $(this).addClass("showed")
                    }    
                })
                if($(window).scrollTop()>($(".new-about-us__capabilities__numbers__card").offset().top - $(window).height() + 150)){
                    let i = 0

                    let imgArr = Array.from($("p.new-about-us__capabilities__numbers__card__description"));

                    function myLoop () {           
                        setTimeout(function () {   
                            imgArr[i].classList.add("showed");          
                            i++;
                            if (i < imgArr.length) {   
                                myLoop();             
                            }                     
                        }, 100)
                    }

                    myLoop();
                }
                $(".new-about-us__projects__row").each(function(){
                    if($(window).scrollTop()>($(this).offset().top - $(window).height() + 50)){
                        $(this).addClass("showed")
                    }    
                })
                $(".new-about-us__mission__content__block").each(function(){
                    if($(window).scrollTop()>($(this).offset().top - $(window).height() + 150)){
                        $(this).addClass("showed")
                        let imgArr = Array.from($(".new-about-us__mission__content__block"));
 
                        imgArr.forEach((elem, idx) => {
                        elem.classList.forEach((elem) => {
                            if (elem === "showed") {
                                $(`.background-img-${idx + 1}`).addClass("showed")
                            }

                           
                    })    
                })
                    }    
                })
                if($(window).scrollTop()>($(".new-about-us__numbers__single-number").offset().top - $(window).height() + 150)){
                    setTimeout(function () {  
                        $(".circle").addClass("showed");
                        $(".first-line").addClass("showed");
                        $(".second-line").addClass("showed");            
                    }, 100);
                }
            })
		});
		
	});
</script>
