<script>
	$(function() {
		$(document).ready(function(){
            $(".new-about-us__offers__content__container__list:nth-child(1)").addClass("showed");
            $(".offers__navigation__title:nth-child(1)").addClass("showed");
            
            let navArr = Array.from($(".offers__navigation__title"));
            let currIndex = 0;


            function changeCase() {
                $(".offers__navigation__title").removeClass("showed")
                navArr[currIndex].classList.add("showed")
                $(`.new-about-us__offers__content__container__list`).removeClass("showed")
                $(`.new-about-us__offers__content__container__list:nth-child(${currIndex + 1})`).addClass("showed")
                if (currIndex < navArr.length -1) {
                    currIndex++
                } else {
                    currIndex = 0;
                }
            }
            let timerId = setInterval( changeCase , 5000);
            
            $(".offers__navigation__title").click(function(event){
                clearInterval(timerId);
                
                $(".offers__navigation__title").removeClass("showed")
                $(this).addClass("showed")

                navArr.forEach((elem, idx) => {
                    elem.classList.forEach((elem) => {
                        if (elem === "showed") {
                            currIndex = idx;
                        }
                    })    
                })
                $(`.new-about-us__offers__content__container__list`).removeClass("showed")
                $(`.new-about-us__offers__content__container__list:nth-child(${currIndex + 1})`).addClass("showed")
            })
		});
		
	});
</script>

